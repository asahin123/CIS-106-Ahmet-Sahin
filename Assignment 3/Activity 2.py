# This program shows an expected age in years,months,days,hours, and seconds.
age = int(input())
years = age
months = 12
days = 365
hours = 8760
seconds = 31536000
print("years=" + str(years))
print("months=" + str(years * months))
print("days=" + str(years * days))
print("hours=" + str(years * hours))
print("seconds=" + str(years * seconds))

